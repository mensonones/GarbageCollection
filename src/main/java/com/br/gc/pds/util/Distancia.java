package com.br.gc.pds.util;

public class Distancia {
	private String lixeira_origem;
	private String lixeira_destino;
	private double distancia;

	public String getLixeira_origem() {
		return lixeira_origem;
	}

	public void setLixeira_origem(String lixeira_origem) {
		this.lixeira_origem = lixeira_origem;
	}

	public String getLixeira_destino() {
		return lixeira_destino;
	}

	public void setLixeira_destino(String lixeira_destino) {
		this.lixeira_destino = lixeira_destino;
	}

	public double getDistancia() {
		return distancia;
	}

	public void setDistancia(double distancia) {
		this.distancia = distancia;
	}

}
